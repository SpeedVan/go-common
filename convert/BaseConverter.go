package convert

// PtrOfInt pointer of int type value
func PtrOfInt(v int) *int {
	return &v
}

// PtrOfInt32 pointer of int32 type value
func PtrOfInt32(v int32) *int32 {
	return &v
}

// PtrOfInt64 pointer of int64 type value
func PtrOfInt64(v int64) *int64 {
	return &v
}

// PtrOfBool pointer of bool type value
func PtrOfBool(v bool) *bool {
	return &v
}

// PtrOfString pointer of string type value
func PtrOfString(v string) *string {
	return &v
}
